#!/usr/bin/env python3
"""A type-annotated function that returns the sum of two floats."""


def add(a: float, b: float) -> float:
    """
    Function that returns the sum of its arguments as a float.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
