#!/usr/bin/env python3
"""A type-annotated function that returns
   the string representation of a float."""


def to_str(n: float) -> str:
    """
    Function that returns the string representation of a given float.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float.
    """
    return str(n)
